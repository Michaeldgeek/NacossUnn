                                        <h3>COS 101 INTRODUCTION TO COMPUTER SCIENCE (2 Units)</h3>
                                        <p>History of Computers Functional Components characteristics of a computer. Problem solving; flowcharts, algorithms. Computer programming statements, symbolic names, arrays, subscripts, expressions; introduction to BASIC or FORTRAN language. ( This course is a required ancillary for many faculties. In exceptional cases, where explicitly specified by NUC, specified versions of the course be offered). </p>
