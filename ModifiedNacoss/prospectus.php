            function fetchDataAjax(link, element) {
                $('#cover').show();
                $('#l').load(link, function (response, status, xhr) {
                    if (status == "success") {
                        $('#cover').hide();
                        $(element).addClass("active-trail active a-aj");
                        $(element).parent().addClass("active-trail active");
                    }
                    else {
                        $('#cover').hide();
                        toast("Could not reach server");
                    }
                });
            }

            function chkAjax(element) {
                if ($(element).hasClass("active")) {
                    // do nothing
                }
                else {
                    $('a.a-aj').parent().removeClass("active-trail active");
                    $('a.a-aj').removeClass("active-trail active p");
                    fetchDataAjax($(element).attr("href"), element);
                }
            }
            function toast(message) {
                var $toast = $('<div class="ui-loader ui-overlay-shadow ui-body-e ui-corner-all"><h3>' + message + '</h3></div>');
                $toast.css({
                    display: 'block',
                    background: '#ccc',
                    opacity: 0.90,
                    color: '#fff',
                    position: 'fixed',
                    padding: '7px',
                    'text-align': 'center',
                    width: '270px',
                    left: ($(window).width() - 284) / 2,
                    top: $(window).height() / 2 - 20
                });

                var removeToast = function () {
                    $(this).remove();
                };
                $toast.click(removeToast);
                $toast.appendTo('body').delay(2000);
                $toast.fadeOut(400, removeToast);
